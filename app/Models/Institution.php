namespace App\Models;





use App\User;
use Illuminate\Database\Eloquent\Model;

class Institution extends Model
{
    protected $fillable = [
        'id',
        'unit_id',
        'name',
        'phone',
        'province'
    ];

    public function users()
    {
        return $this->hasMany(User::class, "inst_id");
    }
}
